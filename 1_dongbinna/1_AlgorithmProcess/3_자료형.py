# 자료형
# 모든 프로그래밍은 결국 데이터를 다루는 행위
#     - 자료형에 대한 이해는 프로그래밍 길에 있어서의 첫 걸음
# 파이썬의 자료형으로는 정수형, 실수형, 복소수형, 문자열, 리스트, 튜플, 사전 등
#     - 파이썬의 자료형은 필수적 암기 필요

# 양의 정수
a = 1000
print(a)

# 음의 정수
a = -7
print(a)

# 0
a = 0
print(a)

# 실수
a = 0.3 + 0.6
print(round(a, 4) == 0.9)

# 파이썬에서 나누기 연산자(/)는 나눠진 결과를 실수형으로 반환